package com.link.controller;

public class LinkDTO
{
	private String link_num, link_title, link_url, link_date, rnum;

	public String getRnum()
	{
		return rnum;
	}

	public void setRnum(String rnum)
	{
		this.rnum = rnum;
	}

	public String getLink_num()
	{
		return link_num;
	}

	public void setLink_num(String link_num)
	{
		this.link_num = link_num;
	}

	public String getLink_title()
	{
		return link_title;
	}

	public void setLink_title(String link_title)
	{
		this.link_title = link_title;
	}

	public String getLink_url()
	{
		return link_url;
	}

	public void setLink_url(String link_url)
	{
		this.link_url = link_url;
	}

	public String getLink_date()
	{
		return link_date;
	}

	public void setLink_date(String link_date)
	{
		this.link_date = link_date;
	}
	
	
}
